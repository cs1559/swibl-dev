class JTEvent extends JLBaseObject {
	
	var $name = null;
	var $description = null;
	var $games = null;
	
	function __construct() {
		parent::__construct();
		$this->games = array();
	}
	
	function setName($name) {
		$this->name = $name;
	}
	
	function getName() {
		return $this->name;
	}
	
	function setDescription($desc) {
		$this->description = $desc;
	}
	function getDescription() {
		return $this->description;
	}
	
	function getGames() {
		return $this->games;
	}
	
		
}
